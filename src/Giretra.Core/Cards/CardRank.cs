namespace Giretra.Core.Cards;

/// <summary>
/// Represents the rank of a card in the 32-card deck.
/// Values are assigned for natural ordering (Seven=7 through Ace=14).
/// </summary>
public enum CardRank
{
    Seven = 7,
    Eight = 8,
    Nine = 9,
    Ten = 10,
    Jack = 11,
    Queen = 12,
    King = 13,
    Ace = 14
}
